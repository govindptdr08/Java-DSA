public class DataTypes {
    public static void main(String[] args) {

        // Integer Data Types
        byte age = 20;
        short year = 2026;
        int salary = 50000;
        long population = 1400000000L;

        // Decimal Data Types
        float height = 5.8f;
        double pi = 3.14159265359;

        // Character Data Type
        char grade = 'A';

        // Boolean Data Type
        boolean isPassed = true;

        // Non-Primitive Data Type
        String name = "Govind";

        // Print All Data Types
        System.out.println("Name = " + name);
        System.out.println("Age (byte) = " + age);
        System.out.println("Year (short) = " + year);
        System.out.println("Salary (int) = " + salary);
        System.out.println("Population (long) = " + population);
        System.out.println("Height (float) = " + height);
        System.out.println("Pi (double) = " + pi);
        System.out.println("Grade (char) = " + grade);
        System.out.println("Passed (boolean) = " + isPassed);
    }
}
